a = [66.25, 333, 334, 1, 1234.5]
print(a)

#reverse: Invierte los datos del array
a.reverse()
print("DATOS INVERTIDOS")
print(a)

#sort: Ordena ascendente
#sort(reverse=True): Ordena descendente
a.sort()
print("DATOS ORDENADOS ASC")
print(a)

a.sort(reverse=True)
print("DATOS ORDENADOS DESC")
print(a)


